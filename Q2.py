import libf
f=lambda x: x/(x+1)


print("Midpoint method:")
l1=libf.midpoint(1,3,5,f)
l2=libf.midpoint(1,3,10,f)
l3=libf.midpoint(1,3,25,f)
print("N          Calculated Result             Actual Result")
print("------------------------------------------------------")
print("5         ",l1,"           1.306852")
print("10        ",l2,"           1.306852")
print("25        ",l3,"            1.306852")

print()
